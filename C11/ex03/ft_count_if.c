int	ft_count_if(char **tab, int lenght, int(*f)(char *))
{
	int	cnt;
	int	i;

	cnt = 0;
	i = 0;

	while (tab[i])
	{
		if (f(tab[i] != 0))
			cnt++;
		i++;
	}
	return (cnt);
}
